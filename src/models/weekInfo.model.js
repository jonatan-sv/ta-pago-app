export default class WeekInfo {
  constructor(month, year, daysCompleted, totalDays, streakCount, streak) {
    this.month = month;
    this.year = year;
    this.daysCompleted = daysCompleted;
    this.totalDays = totalDays;
    this.streakCount = streakCount;
    this.streak = streak;
  }
}
